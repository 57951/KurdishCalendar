namespace KurdishCalendar.Core
{
  /// <summary>
  /// Represents the Gregorian calendar months with Kurmanji Kurdish names,
  /// as used in the Kurdistan Region of Iraq.
  /// </summary>
  public enum KurmanjiGregorianMonth
  {
    /// <summary>
    /// January - Kanûna Duyê (Second Kanun).
    /// </summary>
    KanunaDuye = 1,

    /// <summary>
    /// February - Şubat.
    /// </summary>
    Subat = 2,

    /// <summary>
    /// March - Adar.
    /// </summary>
    Adar = 3,

    /// <summary>
    /// April - Nîsan.
    /// </summary>
    Nisan = 4,

    /// <summary>
    /// May - Gulan.
    /// </summary>
    Gulan = 5,

    /// <summary>
    /// June - Hezîran.
    /// </summary>
    Heziran = 6,

    /// <summary>
    /// July - Tîrmeh.
    /// </summary>
    Tirmeh = 7,

    /// <summary>
    /// August - Tebax.
    /// </summary>
    Tebax = 8,

    /// <summary>
    /// September - Eylûl.
    /// </summary>
    Eylul = 9,

    /// <summary>
    /// October - Çiriya Êkê (First Çiriya).
    /// </summary>
    CiriyaEke = 10,

    /// <summary>
    /// November - Çiriya Duyê (Second Çiriya).
    /// </summary>
    CiriyaDuye = 11,

    /// <summary>
    /// December - Kanûna Êkê (First Kanun).
    /// </summary>
    KanunaEke = 12
  }
}