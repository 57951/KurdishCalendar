namespace KurdishCalendar.Core
{
  /// <summary>
  /// Represents the Gregorian calendar months with Sorani Kurdish names.
  /// </summary>
  public enum SoraniGregorianMonth
  {
    /// <summary>
    /// January - Kanûnî Duhem (Second Kanun).
    /// </summary>
    KanuniDuhem = 1,

    /// <summary>
    /// February - Şubat.
    /// </summary>
    Subat = 2,

    /// <summary>
    /// March - Adar.
    /// </summary>
    Adar = 3,

    /// <summary>
    /// April - Nîsan.
    /// </summary>
    Nisan = 4,

    /// <summary>
    /// May - Ayar.
    /// </summary>
    Ayar = 5,

    /// <summary>
    /// June - Hûzeyran.
    /// </summary>
    Huzeyran = 6,

    /// <summary>
    /// July - Temmûz.
    /// </summary>
    Temmuz = 7,

    /// <summary>
    /// August - Ab.
    /// </summary>
    Ab = 8,

    /// <summary>
    /// September - Eylûl.
    /// </summary>
    Eylul = 9,

    /// <summary>
    /// October - Tişrînî Yekem (First Tişrîn).
    /// </summary>
    TisriniYekem = 10,

    /// <summary>
    /// November - Tişrînî Duhem (Second Tişrîn).
    /// </summary>
    TisriniDuhem = 11,

    /// <summary>
    /// December - Kanûnî Yekem (First Kanun).
    /// </summary>
    KanuniYekem = 12
  }
}