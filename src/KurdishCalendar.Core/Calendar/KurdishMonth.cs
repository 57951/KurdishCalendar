namespace KurdishCalendar.Core
{
  /// <summary>
  /// Represents the months in the Kurdish solar calendar.
  /// </summary>
  public enum KurdishMonth
  {
    /// <summary>
    /// First month (31 days) - Spring equinox month, begins around 21 March.
    /// </summary>
    Xakelêwe = 1,

    /// <summary>
    /// Second month (31 days) - Spring.
    /// </summary>
    Gulan = 2,

    /// <summary>
    /// Third month (31 days) - Spring.
    /// </summary>
    Cozerdan = 3,

    /// <summary>
    /// Fourth month (31 days) - Summer.
    /// </summary>
    Puşper = 4,

    /// <summary>
    /// Fifth month (31 days) - Summer.
    /// </summary>
    Gelawêj = 5,

    /// <summary>
    /// Sixth month (31 days) - Summer.
    /// </summary>
    Xermanan = 6,

    /// <summary>
    /// Seventh month (30 days) - Autumn.
    /// </summary>
    Rezber = 7,

    /// <summary>
    /// Eighth month (30 days) - Autumn.
    /// </summary>
    Gelarêzan = 8,

    /// <summary>
    /// Ninth month (30 days) - Autumn.
    /// </summary>
    Sermawez = 9,

    /// <summary>
    /// Tenth month (30 days) - Winter.
    /// </summary>
    Befranbar = 10,

    /// <summary>
    /// Eleventh month (30 days) - Winter.
    /// </summary>
    Rêbendan = 11,

    /// <summary>
    /// Twelfth month (29 or 30 days in leap years) - Winter.
    /// </summary>
    Reşeme = 12
  }
}